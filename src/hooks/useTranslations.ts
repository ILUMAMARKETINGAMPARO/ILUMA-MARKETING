export const useTranslations = () => {
  const { language, t } = useLanguage();

  const getTranslation = (key: string) => {
    // Essaie d'abord les traductions de pages sp√©cifiques
    const pageTranslation = pageTranslations[language as keyof typeof pageTranslations]?.[key];
    if (pageTranslation) {
      return pageTranslation;
    }

    // Essaie ensuite la traduction normale
    try {
      const translation = t(key);
      if (translation && translation !== key) {
        return translation;
      }
    } catch (error) {
      // Silently continue to fallback
    }

    // Utilise la traduction de fallback
    const fallback = fallbackTranslations[language as keyof typeof fallbackTranslations]?.[key];
    return fallback || key;
  };

  return { t: getTranslation, language };
};

export default useTranslations;